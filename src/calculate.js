/**
 * Description: function to add two numbers 
 * @param   {type: Number} num1: First number input, 
 * @param   {type: Number} num2: Second number input
 * @return  {type: Number} result
 */
export const add = (num1, num2) => {
    return num1 + num2;
}
/**
 * Description: function to substract two numbers 
 * @param   {type: Number} num1: First number input, 
 * @param   {type: Number} num2: Second number input
 * @return  {type: Number} result
 */
export const substract = (num1, num2) => {
    return num1 - num2;
}
/**
 * Description: function to multiply two numbers 
 * @param   {type: Number} num1: First number input, 
 * @param   {type: Number} num2: Second number input
 * @return  {type: Number} result
 */
export const multiply = (num1, num2) => {
    return num1 * num2;
}
